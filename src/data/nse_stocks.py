# ── Build combined lists ─────────────────────────────────────────────

_NSE_RAW = NIFTY_50 + NIFTY_NEXT_50 + NIFTY_MIDCAP_150 + POPULAR_SMALLCAPS
_NSE_BASE_NAMES = {s.upper() for s in _NSE_RAW}

NSE_STOCKS = sorted(list({f"{s}.NS" for s in _NSE_RAW}))

BSE_STOCKS = sorted(list({
    f"{s}.BO" for s in BSE_ONLY if s.upper() not in _NSE_BASE_NAMES
}))

ALL_INDIAN_STOCKS = sorted(NSE_STOCKS + BSE_STOCKS)

TOTAL_NSE_STOCKS = len(NSE_STOCKS)
TOTAL_BSE_STOCKS = len(BSE_STOCKS)
TOTAL_STOCKS = len(ALL_INDIAN_STOCKS)


def search_nse_stocks(query: str, limit: int = 20) -> list[dict]:
    """Search across all Indian stocks (NSE + BSE). Prefix matches first."""
    if not query or not query.strip():
        return []
    q = query.strip().upper()
    prefix_matches: list[dict] = []
    substring_matches: list[dict] = []
    seen: set[str] = set()
    for symbol in ALL_INDIAN_STOCKS:
        if symbol.endswith(".NS"):
            base = symbol.removesuffix(".NS")
            exchange = "NSE"
        else:
            base = symbol.removesuffix(".BO")
            exchange = "BSE"
        if base in seen:
            continue
        base_upper = base.upper()
        if base_upper.startswith(q) or symbol.upper().startswith(q):
            prefix_matches.append({"symbol": symbol, "name": base, "exchange": exchange})
            seen.add(base)
        elif q in base_upper:
            substring_matches.append({"symbol": symbol, "name": base, "exchange": exchange})
            seen.add(base)
    result = prefix_matches + substring_matches
    return result[:limit]
